package com.upskill.java_1;

public class AccessModifier {

	public String Name = "Amin";  //any class can access
	private int age = 30;   //only same class can access
	protected int ssn = 123456789;   // classes in the same package and subclasses can access 
	 String address = "Thomas Street"; // If you don't put any access modifier, java takes it as default. Class in same package can access.  
}






