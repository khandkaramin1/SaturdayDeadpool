package com.upskill.assignment_2_1;

public class For_Loop {

	public static void main(String[] args) {
		//1) Write a for loop that prints 50 through 1 separated by spaces in the same line. 
		
		fiftyLoop();
	}
	

public static void fiftyLoop(){  //For loop - Do again and again upto upper limit
	for(int count= 50; count >= 1; count--) 
	{ 
		System.out.print(count + " "); }		
}
	}





