package com.upskill.java_2;

public class loop_Practice {
	
	/* Types of Loops
	 * 1. For Loops 
	 * 2. While Loops 
	 * 3. Do While Loops 
	 * 4. Infinite Loop
	 */
	
	public static void main (String[] args){
		practiceforloop();
	}
	
	public static void practiceforloop(){  //For loop - Do again and again upto upper limit
		int i;
		for (i =0; i<=80; i+=8){
			System.out.println("" +i);
		}
	}

}
